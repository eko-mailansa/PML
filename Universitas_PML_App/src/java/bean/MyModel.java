package bean;

/**
 *
 * @author Home
 */
public class MyModel {

    private int id;
    private String nama;
    private int kelas;
    private int mak;

    public MyModel(int id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getKelas() {
        return kelas;
    }

    public void setKelas(int kelas) {
        this.kelas = kelas;
    }

    public int getMak() {
        return mak;
    }

    public void setMak(int mak) {
        this.mak = mak;
    }

}
